DROP TABLE MEMBER IF EXISTS;
CREATE TABLE MEMBER (
    MEMBER_ID VARCHAR_IGNORECASE(50) NOT NULL PRIMARY KEY,
    PASSWORD VARCHAR_IGNORECASE(50) NOT NULL,
    NAME VARCHAR_IGNORECASE(50) NOT NULL,
    AGE INTEGER NOT NULL,
);


DROP TABLE MEMBERS_AUTHORITY IF EXISTS;
CREATE TABLE MEMBERS_AUTHORITY (
    MEMBERS_AUTHORITY_ID INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    MEMBER_ID VARCHAR_IGNORECASE(50) NOT NULL ,
    AUTHORITY_ID VARCHAR_IGNORECASE(50) NOT NULL
);


DROP TABLE AUTHORITY IF EXISTS;
CREATE TABLE AUTHORITY (
    AUTHORITY_ID VARCHAR_IGNORECASE(50) NOT NULL PRIMARY KEY,
    NAME VARCHAR_IGNORECASE(50) NOT NULL,
    ENABLED BOOLEAN NOT NULL
);


DROP TABLE AUTHORITYS_FUNCTION IF EXISTS;
CREATE TABLE AUTHORITYS_FUNCTION (
    AUTHORITYS_FUNCTION_ID INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    AUTHORITY_ID VARCHAR_IGNORECASE(50) NOT NULL,
    FUNCTION_ID INTEGER NOT NULL
);


DROP TABLE FUNCTION IF EXISTS;
CREATE TABLE FUNCTION (
    FUNCTION_ID INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    PARENT_FUNCTION_ID INTEGER,
    NAME VARCHAR_IGNORECASE(50) NOT NULL,
    TYPE VARCHAR_IGNORECASE(50) NOT NULL,
    PATH VARCHAR_IGNORECASE(50),
    METHOD VARCHAR_IGNORECASE(50),
    ENABLED BOOLEAN NOT NULL
);